"""Create a PPT-cover GIF for an approach-case run.

The cover stacks the oblique mesh animation above a compact Fig. 6 force
chart with explicit x/y axis labels.  It expects the one-window diagnostic
frames generated by ``Approach/1window_gif.py``.
"""

